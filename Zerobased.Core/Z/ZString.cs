namespace Zerobased
{
    public static class ZString
    {
        /// <summary>
        /// Represents space symbol as string
        /// </summary>
        public const string Space = " ";

        /// <summary>
        /// Represents '*' as string
        /// </summary>
        public const string Star = "*";

        /// <summary>
        /// Represents ',' as string
        /// </summary>
        public const string Comma = ",";

        /// <summary>
        /// Represents '#' as string
        /// </summary>
        public const string Sharp = "#";

        /// <summary>
        /// Represents '(' as string
        /// </summary>
        public const string OpeningBracket = "(";

        /// <summary>
        /// Represents ')' as string
        /// </summary>
        public const string ClosingBracket = ")";

        /// <summary>
        /// Represents '&' as string
        /// </summary>
        public const string Ampersant = "&";

        /// <summary>
        /// Represents '?' as string
        /// </summary>
        public const string Question = "?";

        /// <summary>
        /// Represents '%' as string
        /// </summary>
        public const string Percent = "%";

        /// <summary>
        /// Represents '_' as string
        /// </summary>
        public const string Underscope = "_";

        /// <summary>
        /// Represents '.' as string
        /// </summary>
        public const string Dot = ".";
    }
}
