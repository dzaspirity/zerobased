        /// Represents horisontal tabulation char
        /// </summary>
        public const char Tab = '\t';

        /// <summary>
        /// Represents space char
        /// </summary>
        public const char Space = ' ';

        /// <summary>
        /// Represents char '*'
        /// </summary>
        public const char Star = '*';

        /// <summary>
        /// Represents char '.'
        /// </summary>
        public const char Dot = '.';

        /// <summary>
        /// Represents char ','
        /// </summary>
        public const char Comma = ',';

        /// <summary>
        /// Represents char '+'
        /// </summary>
        public const char Plus = '+';

        /// <summary>
        /// Represents char '-'
        /// </summary>
        public const char Minus = '-';
    }
}
